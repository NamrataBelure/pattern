package String;

public class strLong {
		   public static void main(String[] args)
		   {
		       String str = "24000";
		       String str2 = "-21683";
		       //Conversion using parseLong(String) method
		       long num = Long.parseLong(str);
		       long num2 = Long.parseLong(str2);
		       System.out.println(num+num2);		
		   }
		}

